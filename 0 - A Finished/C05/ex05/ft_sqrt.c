int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0 || nb > 2147395600)
		return (0);
	i = 1;
	while (i <= nb / i)
	{
		if (i * i == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
